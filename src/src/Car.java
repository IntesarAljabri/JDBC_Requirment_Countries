package src;

import java.util.List;

public class Car {

	List<String> signs;
	String side;

	// Setter and getter
	public List<String> getSigns() {
		return signs;
	}

	public void setSigns(List<String> signs) {
		this.signs = signs;
	}

	public String getSide() {
		return side;
	}

	public void setSide(String side) {
		this.side = side;
	}

}
